#Entrada de datos

Valor = int(input("Ingrese un valor: "))

#Variables

Contador = 0

Numero = Valor

#Proceso y salida de datos
while Contador < 10:
    if Numero % Valor == 0:
        print("Los numeros divisibles son: ", Numero)

        Contador += 1

    Numero += 1















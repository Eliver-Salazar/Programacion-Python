# Ejemplo con una cadena de texto
texto = "Hola, ¿cómo estás?"
longitud_texto = len(texto)
print("La longitud de la cadena de texto es:", longitud_texto)
# Salida: La longitud de la cadena de texto es: 18

# Ejemplo con una lista
numeros = [1, 2, 3, 4, 5]
cantidad_numeros = len(numeros)
print("La cantidad de elementos en la lista es:", cantidad_numeros)
# Salida: La cantidad de elementos en la lista es: 5

# Ejemplo con un conjunto
conjunto = {10, 20, 30, 40, 50}
cantidad_elementos = len(conjunto)
print("La cantidad de elementos en el conjunto es:", cantidad_elementos)
# Salida: La cantidad de elementos en el conjunto es: 5

# Ejemplo con un diccionario
diccionario = {"nombre": "Juan", "edad": 30, "ciudad": "Madrid"}
cantidad_claves = len(diccionario)
print("La cantidad de claves en el diccionario es:", cantidad_claves)
# Salida: La cantidad de claves en el diccionario es: 3




